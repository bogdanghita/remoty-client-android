package com.example.dcwa.auxclasses;

public class ButtonParameters {

	public int rowStart, rowStop, columnStart, columnStop;
	
	public ButtonParameters(int rowStart, int rowStop, int columnStart, int columnStop) {
		this.rowStart = rowStart;
		this.rowStop = rowStop;
		this.columnStart = columnStart;
		this.columnStop = columnStop;
	}
	
}
